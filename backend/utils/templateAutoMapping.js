function compact(value = '') {
  return String(value || '')
    .replace(/주식회사|\(주\)|㈜|（주）/g, '')
    .replace(/[\s._\-()（）\[\]{}·ㆍ,:：/\\]+/g, '')
    .toLowerCase();
}

function toUpperText(...values) {
  return values.filter(Boolean).map((value) => String(value)).join(' ').toUpperCase();
}

function detectTemplateKind(mappingJson = {}, template = {}) {
  const raw = toUpperText(
    mappingJson.template_type,
    mappingJson.templateType,
    mappingJson.layout,
    mappingJson.layoutType,
    mappingJson.documentKind,
    template.template_type,
    template.templateType,
    template.template_name,
    template.templateName,
    template.template_code,
    template.templateCode,
    template.description
  );
  const ko = compact([
    mappingJson.title,
    mappingJson.name,
    mappingJson.sheetName,
    mappingJson.sheet,
    template.template_name,
    template.templateName,
    template.description,
  ].filter(Boolean).join(' '));

  if (raw.includes('CUSTOM_DOCUMENT_FORM') || raw.includes('DOCUMENT_FORM') || /문서양식|회사양식|결재양식/.test(ko)) return 'CUSTOM_DOCUMENT_FORM';
  if (raw.includes('MEETING') || /회의록|회의|안건|결정사항|조치사항/.test(ko)) return 'MEETING_MINUTES';
  if (raw.includes('OFFICIAL') || /공문|수신|참조|발신|시행/.test(ko)) return 'OFFICIAL_LETTER';
  if (raw.includes('REPORT') || raw.includes('SECTION') || raw.includes('APPROVAL') || /보고서|보고|검토|현황|리포트/.test(ko)) return 'REPORT';
  if (raw.includes('COMPARISON') || raw.includes('VENDOR') || /비교|업체별|회사별|거래처별/.test(ko)) return 'COMPARISON_MATRIX';
  if (raw.includes('ESTIMATE') || /견적/.test(ko)) return 'ESTIMATE';
  if (raw.includes('UNIT_PRICE') || raw.includes('PRICE') || /단가표|가격표|표준시장단가|단가/.test(ko)) return 'UNIT_PRICE_TABLE';
  if (raw.includes('WORK_LOG') || /작업일보|일보|투입인원|작업내용/.test(ko)) return 'WORK_LOG_TABLE';
  return 'NORMAL_TABLE';
}

function mapping(fieldKey, label, sourcePath, fallbackPaths = [], extra = {}) {
  return {
    fieldKey,
    label,
    mappingType: 'AUTO_VALUE',
    sourcePath,
    fallbackPaths,
    autoGenerated: true,
    ...extra,
  };
}

