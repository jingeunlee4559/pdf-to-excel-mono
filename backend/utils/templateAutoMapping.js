function mergeMappings(existing = [], additions = []) {
  const out = Array.isArray(existing) ? existing.filter(Boolean) : [];
  const seen = new Set(out.map((item) => String(item.fieldKey || item.field_key || '').trim()).filter(Boolean));
  additions.forEach((item) => {
    const key = String(item.fieldKey || item.field_key || '').trim();
    if (!key || seen.has(key)) return;
    seen.add(key);
    out.push(item);
  });
  return out;
}

function ensureTemplateMappingJson(mappingJson = {}, template = {}) {
  const base = mappingJson && typeof mappingJson === 'object' ? { ...mappingJson } : {};
  const kind = detectTemplateKind(base, template);
  const preset = PRESETS[kind] || PRESETS.NORMAL_TABLE;
  const out = {
    ...preset,
    ...base,
    template_type: base.template_type || base.templateType || preset.template_type,
    sheetName: base.sheetName || base.sheet || template.default_sheet_name || template.defaultSheetName || preset.sheetName,
    layout: base.layout || base.layoutType || preset.layout,
    title: base.title || template.template_name || template.templateName || preset.title,
    autoMappingVersion: '2026-06-24-v2',
    autoValueFallback: true,
  };
  out.sections = Array.isArray(base.sections) && base.sections.length ? base.sections : (preset.sections || []);
  out.headerPairs = Array.isArray(base.headerPairs) && base.headerPairs.length ? base.headerPairs : (preset.headerPairs || []);
  out.approvalLines = Array.isArray(base.approvalLines) && base.approvalLines.length ? base.approvalLines : (preset.approvalLines || []);
  out.baseColumns = Array.isArray(base.baseColumns) && base.baseColumns.length ? base.baseColumns : (preset.baseColumns || []);
  out.repeatGroups = Array.isArray(base.repeatGroups) && base.repeatGroups.length ? base.repeatGroups : (preset.repeatGroups || []);
  out.summaryColumns = Array.isArray(base.summaryColumns) && base.summaryColumns.length ? base.summaryColumns : (preset.summaryColumns || []);
  out.mappings = mergeMappings(base.mappings, preset.mappings || []);
  if (!out.mappings.length && COMMON_HEADER_MAPPINGS.length) out.mappings = [...COMMON_HEADER_MAPPINGS];
  return out;
}

function hasRealExcelCellMapping(mappingJson = {}) {
  const mappings = Array.isArray(mappingJson.mappings) ? mappingJson.mappings : [];
  return mappings.some((m) => m && (
    m.cellAddress || m.mergedRange || m.columnLetter ||
    (Array.isArray(m.columnLetters) && m.columnLetters.length) ||
    (Array.isArray(m.groupRanges) && m.groupRanges.length)
  ));
}

